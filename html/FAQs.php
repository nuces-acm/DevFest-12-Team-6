<b>Q. What is this site about?</b>
A. This site is all about donating the deserving people in Pakistan. 
All a donator is to do is submit a form, fax, e-mail or call us 
and then donate within 1-week of time. The deserving people will 
we helped out with the money donated.

<b>Q. Why should we put belief in you?</b>
A. It is because HopeFoundation.com is a registered charity website.

<b>Q. How could I, as a donor, be assured that the person who I am 
donating to is really deserving?</b>
A. For the assurance of the deserving person, we put the pictures 
and videos of the person who really deserves your help.

<b>Q. What type of cases do you handle?</b>
A. Our aim is to handle every case related to the help of humanity; 
but currently we help the deserving people who are in hospital, 
or the ones who are the victim of any natural disaster,  

<b>Q. Why is there ranking for donors?</b>
A. It is only to acknowledge and appreciate the donors.

<b>Q. I want to donate but I want to remain anonymous, can I?
A. Yes, while donating you can enable a check-box to put you into 
anonymous category.

<b>Q. What are the ways to donate?</b>
A. You can donate with Paypal, MoneyBooker, Western Union, Credit 
Card, EasyPaisa, any money transfer service or you can even donate
 by going in any of our donation center near to your city.

<b>Q. Where are your donation centers in Pakistan?</b>
A. Please visit the contact us page for them.

<b>Q. If a donor submits a form to donate $100 and he donates after 5 days
A. and in the span of 5 days another donor donates all the money needed,
then what will happen with money the first donor gave?</b>
The money of the first donor will either be given to any other deserving
person or back to the donor, depending upon the choice the donor selected
 while submitting the donation form.
